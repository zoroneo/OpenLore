const DIM = 8;

function makeVector(seed: number): number[] {
  return Array.from({ length: DIM }, (_, i) => ((seed + i) % 10) * 0.1);
}

function makeMockEmbedSvc(): EmbeddingService {
  const callCount = 0;
  return {
    embed: vi.fn().mockImplementation(async (texts: string[]) =>
      texts.map((_, i) => makeVector(callCount + i++))
    ),
  } as unknown as EmbeddingService;
}

// ============================================================================
// HELPERS
// ============================================================================

async function createSpecsDir(
  root: string,
  specs: Record<string, string>
): Promise<string> {
  const specsDir = join(root, 'openspec', 'specs');
  for (const [domain, content] of Object.entries(specs)) {
    const domainDir = join(specsDir, domain);
    await mkdir(domainDir, { recursive: true });
    await writeFile(join(domainDir, 'spec.md'), content, 'utf-8');
  }
  return specsDir;
}

